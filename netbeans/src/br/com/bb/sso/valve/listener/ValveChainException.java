package br.com.bb.sso.valve.listener;


/**
 * Exceção lançada por ouvintes de AuthenticationValve
 * para interromper o fluxo da conexão no tomcat.
 * @author Juno Roesler - F6036477
 */
public class ValveChainException extends Exception {

	/**
	 * Contrutor padrão sem argumentos
	 */
	public ValveChainException() {
	}


	/**
	 * Construtor que recebe a mensagem da exceção.
	 * @param message Mensagem da exceção.
	 */
	public ValveChainException(String message) {
		super(message);
	}


	/**
	 * Construtor que recebe a mensagem e causa da exceção.
	 * @param message Mensagem da exceção.
	 * @param cause Causa da exceção.
	 */
	public ValveChainException(String message, Throwable cause) {
		super(message, cause);
	}


	/**
	 * Construtor que recebe a causa da exceção.
	 * @param cause Causa da exceção.
	 */
	public ValveChainException(Throwable cause) {
		super(cause);
	}

}
