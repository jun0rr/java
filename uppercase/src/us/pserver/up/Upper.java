  private void editAreaKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_editAreaKeyReleased
    updateText();
    
    if(evt.getKeyCode() != KeyEvent.VK_C
        || !evt.isControlDown())
      return;
    
    int selstart = editArea.getSelectionStart();
    int selend = editArea.getSelectionEnd();
    if(selstart == selend)
      copy.setText(upperArea.getText()).putInClipboard();
    else
      copy.setText(editArea.getSelectedText()).putInClipboard();
  }//GEN-LAST:event_editAreaKeyReleased

  
