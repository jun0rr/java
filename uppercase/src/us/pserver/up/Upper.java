  public void updateText() {
    String text = editArea.getText();
    boolean upper = upperCheck.isSelected();
    
    //(?<=(FROM|JOIN) ).+(WHERE|JOIN|;|$)
    //[a-zA-Z0-9_]+[\\.]{1}
    //(?<=AS[\s]).+\b
    text = (upper ? text.toUpperCase() : text.toLowerCase());
    Pattern pt = Pattern.compile("(?<=(FROM|JOIN)[\\s]).+(WHERE|JOIN|;|$|\n|\r)");
    Matcher mt = pt.matcher(text);
    while(mt.find()) {
      String sm = text.substring(mt.start(), mt.end());
      sm = (upper ? sm.toLowerCase() : sm.toUpperCase());
      text = text.substring(0, mt.start()) + sm + text.substring(mt.end());
    }
    pt = Pattern.compile("[a-zA-Z0-9_]+[\\.]{1}");
    mt = pt.matcher(text);
    while(mt.find()) {
      String sm = text.substring(mt.start(), mt.end());
      sm = (upper ? sm.toLowerCase() : sm.toUpperCase());
      text = text.substring(0, mt.start()) + sm + text.substring(mt.end());
    }
    pt = Pattern.compile("(?<=\\$)[a-zA-Z0-9_]+");
    mt = pt.matcher(text);
    while(mt.find()) {
      String sm = text.substring(mt.start(), mt.end());
      sm = (upper ? sm.toLowerCase() : sm.toUpperCase());
      text = text.substring(0, mt.start()) + sm + text.substring(mt.end());
    }
    
    upperArea.setText(text);
    if(text != null && !text.trim().isEmpty() 
        && autoCheck.isSelected())
      copy.setText(upperArea.getText()).putInClipboard();
    
    SwingUtilities.invokeLater(()->{
      upperArea.setSelectionStart(0);
      upperArea.setSelectionEnd(upperArea.getText().length());
      upperArea.select(0, upperArea.getText().length());
      upperArea.paint(upperArea.getGraphics());
    });
  }
  

